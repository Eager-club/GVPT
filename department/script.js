// Function to render the navbar
function renderNavLinks() {
    const navLinksElement = document.getElementById('navLinks');

    departmentData.navLinks.forEach((link, index) => {
        const listItem = document.createElement('li');
        listItem.textContent = link.name;
        listItem.dataset.index = index;

        if (typeof link.content === 'object') {
            listItem.classList.add('has-sublinks');
        }

        listItem.addEventListener('click', () => {
            renderContent(index);
        });

        navLinksElement.appendChild(listItem);
    });
}

// Function to render the content area
function renderContent(index) {
    const contentArea = document.getElementById('contentArea');
    const linkData = departmentData.navLinks[index];

    if (typeof linkData.content === 'object') {
        contentArea.innerHTML = JSON.stringify(linkData.content, null, 2).replace(/"/g, '').replace(/,/g, '<br>');
    } else {
        contentArea.textContent = linkData.content;
    }
}

// Initial render of navbar and default content
renderNavLinks();
renderContent(0);

